class DoublyLinkedListNode:
    def __init__(self, node_data):
        self.data = node_data
        self.next = None
        self.prev = None

class DoublyLinkedList:
    def __init__(self):
        self.head = None
        self.tail = None

    def insert_node(self, node_data):
        node = DoublyLinkedListNode(node_data)

        if not self.head:
            self.head = node
        else:
            self.tail.next = node
            node.prev = self.tail


        self.tail = node

def print_doubly_linked_list(node, sep, fptr):
    while node:
        fptr.write(str(node.data))

        node = node.next

        if node:
            fptr.write(sep)


def reverse(llist):
    prev = None
    head = llist
    
    # Loop while head node
    while head:     
        # Create buffer for next node for while loop
        next_head = head.next
        head.next = prev # Next is now previous
        prev = head # Previous is now next
        head = next_head # Set next head for while loop
    
    # Return last node in list
    return prev

